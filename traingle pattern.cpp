#include<stdio.h>

int main()
{
	int i,j,k,l=0,m,p=0;
	for(i=1;i<=5;i++)               //raw
	{
		for(j=1;j<=i;j++)            //coluum
		{
			printf("1");
			}
			for(k=5;k>=l;k--){
				printf("2");
				
				}
				for(m=5;m>=p;m--){
					printf("3");
					
		}
		
		for(int z=0;z<=l;z++){
			printf("4");
		}
		l++;
		p++;
		printf("\n");
	}
	
}